    private void DangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DangNhapActionPerformed
        // TODO add your handling code here:
        Connect_DB cn = new Connect_DB();
        
        
        String email = EmailTxT.getText();
        char[] matKhauChars = PassTxT.getPassword();
        String matKhau = new String(matKhauChars);
        try{
            Connection conn = cn.getConnection();
            if(email.equals("") || matKhauChars.equals("")){
                JOptionPane.showMessageDialog(this,"Bạn phải nhập đủ thông tin");
            }
            else{
                String afteremail = "@gmail.com";
                if(!email.endsWith(afteremail))
                {
                    JOptionPane.showMessageDialog(this,"Email nhập phải có dạng example@gmail.com");
                }
                else{
                    int atIndex = email.indexOf(afteremail);
                    String userName = email.substring(0, atIndex);
                    if(userName.isEmpty()){
                       JOptionPane.showMessageDialog(this,"Email bạn nhập không hợp lệ");
                    }
                    else{
                        if(matKhau.length() < 8 || !matKhau.matches(".*\\d+.*")){
                            JOptionPane.showMessageDialog(this,"Mật khẩu không hợp lệ");
                        }
                        else{
                            String timEmail = "select Email from TaiKhoan where Email= ? And tkpassword=?";
                            PreparedStatement pst = conn.prepareCall(timEmail);
                            pst.setString(1,email);
                            pst.setString(2,matKhau);
                            ResultSet rs = pst.executeQuery();
                            if(rs.next()){
                                JOptionPane.showMessageDialog(this,"Đăng nhập thành công");
                                this.setVisible(false);
                                Loading loading = new Loading();
                                loading.setVisible(true);
                                ToanCuc tt = new ToanCuc();
                                tt.setEmail(email);
                                String accountName = getAccountName(email);
                                tt.setTenTK(accountName);
                            }
                            else{
                                JOptionPane.showMessageDialog(this,"Sai Email hoặc mật khẩu"); 
                            }
                            
                        }
                       }
                }
            }
        } catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Sai tên tài khoản hoặc mật khẩu");
        }
    }//GEN-LAST:event_DangNhapActionPerformed

