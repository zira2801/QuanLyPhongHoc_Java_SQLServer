    private String getAccountName(String email) {
         Connect_DB cn = new Connect_DB();
        try {
            Connection conn = cn.getConnection();
            // Truy vấn cơ sở dữ liệu để lấy tên tài khoản
            String query = "SELECT Ten FROM TaiKhoan WHERE Email = ?";
            PreparedStatement statement = conn.prepareCall(query);
            statement.setString(1, email);
            ResultSet resultSet = statement.executeQuery();
            resultSet.next();
            return resultSet.getString("Ten");
        } catch (SQLException e) {
        }
        return "";
    }
